#need input matrices as vectors => flatten. x has shape (5, 10, 16)
def init_vectors(x):
    arr = x
    for i in range(0,len(x)):
        x[i] = np.array(x[i])
        x[i] = x[i].flatten()
        arr[i] = x[i]
    return arr


#From the formula, eq 2.26
def init_weight_matrix(x):
    N = len(x[0])
    W = np.zeros((N,N))
    for i in range(0, len(W)):
        for j in range(0, len(W[0])):
            if i == j:
                W[i][j] = 0
                continue
            sum_outer = 0
            for pattern in x:
                sum_outer += pattern[i]*pattern[j]
            W[i][j] = (1/N)*sum_outer
    return W


def classify(patterns, x_transformed):
    shape = (len(x_transformed),len(x_transformed[0]))
    for i in range(0,len(patterns)):
        patterns[i] = np.reshape(patterns[i],shape)
        if np.array_equiv(patterns[i], x_transformed):
            return i+1
        elif np.array_equiv(-patterns[i], x_transformed):
            return -(i+1)
    return 6


